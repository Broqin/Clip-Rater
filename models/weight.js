export class Weight {
    /**
     * 
     * @param {Number} id - A whole number. 
     * @param {String} name - Name of the weight.
     * @param {Number} value - A positive or negative floating-point number.
     */
    constructor(id, name, value) {
        this.id = id;
        this.name = name;
        this.value = value;
    }
}